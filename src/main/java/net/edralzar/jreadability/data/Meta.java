package net.edralzar.jreadability.data;

public class Meta {
	private int num_pages;
	private int page;
	private int item_count_total;
	private int item_count;

	public int getNumPages() {
		return num_pages;
	}

	public int getPage() {
		return page;
	}

	public int getItemCountTotal() {
		return item_count_total;
	}

	public int getItemCount() {
		return item_count;
	}

}
